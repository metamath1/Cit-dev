# lab 30
n = 5
w = (n-1)*2 + 1 

for i in range(n):
    s =  '*' * (i*2+1)
    margin = w - len(s)
    margin = ' ' * int(margin / 2)
    print(margin, s, margin, sep='')
##################################################################### 

# 2중 for 버전
for i in range(n):
    s = '*' * (i*2+1)
    b = ''
    for j in range(n-1-i, 0, -1):
        b += ' '
    print(b + s)
